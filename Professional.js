 class Professional{

       constructor (name, age, genre, weight, height, hairColor, eyeColor, race, isRetired, nationality, oscarNumbers, profession, foto) {
        this.name = name;
        this.age = age;
        this.genre = genre;
        this.weight = weight;
        this.height = height;
        this.hairColor = hairColor;
        this.eyeColor = eyeColor;
        this.race = race;
        this.isRetired = isRetired;
        this.nationality = nationality;
        this.oscarNumbers = oscarNumbers;
        this.profession = profession;
        this.foto=foto;
    }
}

module.exports.Professional=Professional;